#include "libft.h"

void	ft_striteri(char *str, void (*f)(unsigned int, char *))
{
	unsigned int	str_len;
	unsigned int	i;

	str_len = ft_strlen(str);
	i = 0;
	while (i < str_len)
	{
		f(i, str + i);
		i++;
	}
}
